# Arithmetic

# Addition
result = 5 + 10
print(f" 5 + 10 = {result}")
result += 2 # same as result = result + 2
print(f" result = {result}")

# Subtraction
result = 45 - 12
print(f" 45 - 12 = {result}")
result -= 3 # same as result = result - 3
print(f" result = {result}")

# Multiplication
result = 5 * 12
print(f" 5 * 12 = {result}")
result *= 2 # same as result = result * 2
print(f" result = {result}")

# Division
result = 12 / 4
print(f" 12 / 4 = {result}")
result /= 3 # same as result = result / 3
print(f" result = {result}")

# Floor - rounding down to a whole number
result = 15 // 2
# 15 / 2 = 7 remainder 1 
# 15 / 2 = 7.5
print(f" 15 // 2 = {result}")
result //= 2 # same as result = result // 2
print(f" result = {result}")

# Modulus - take the remainder
result = 15 % 2
# 15 % 2 = 7 remainder 1 
# 15  2 = 1
print(f" 15 % 2 = {result}")
result %= 2 # same as result = result % 2
print(f" result = {result}")

